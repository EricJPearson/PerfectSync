    <p> ...and more!  The <span class="Highlight">Pro</span> version even allows you to access low level API functions that require ODBC Handles and memory pointers to SQL Tools data buffers.  (But only if you want to.   The use of handles is <em>never</em> required.)</p>
    <p><img src="/rt/Dingbats/Divider.A.512.gif" width="512" height="8" /></p>
    <blockquote>
      <blockquote>
        <p align="left" class="Tiny">       All of the SQL Tools functions that are described here are subject to the limitations of the ODBC driver that you choose to use. SQL Tools cannot support features that are not supported by your ODBC driver. Most modern ODBC drivers provide most of the functions that are described in this document, but Perfect Sync cannot guarantee that every feature listed here will be available to every program. Think of it this way... Your word processor may support color printing, but if your printer doesn't support color then all you will see is black lettering on white paper. The same is true for your SQL Tools programs and your ODBC driver. SQL Tools can't do things that your database and/or driver don't support.</p>
