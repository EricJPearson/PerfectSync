    <p align="center" class="TopicHeader1">To figure out which version <em>you</em> are currently using...    </p>
    <ol>
      <ol>
            <ol>
              <li>Determine the Development Tool's DLL file name. Consult the product documentation under &quot;Installation&quot; for the exact file name. <span class="Tiny">(The file names all follow the pattern GfxT_Pro.DLL, GfxT_Std.DLL, SQLT_Pro.DLL, CT_Pro.DLL, etc. The underscore  is sometimes a number, like SQLT3Pro.DLL)</span> </li>
              <li>Use Microsoft Windows Explorer (not Internet Explorer) to locate your Development Tool DLL. We recommend using Windows Explorer's Search feature to scan all of your hard drives and network drives, because DLLs are often copied from place to place. <span class="Tiny">(A common source of &quot;version problems&quot; is a forgotten DLL in an out-of-the-way folder.)</span> </li>
              <li>Once you have located a DLL, right-click the file and select Properties from the popup.</li>
              <li>Select the Version tab. </li>
              <li>The version number will appear near the top of the popup. Here are typical Version displays from Windows XP and Windows 7:</li>
            </ol>
      </ol>
      <blockquote>
        <p align="center"><img src="/rt/DevTools/DLL_Version_XP.PNG" alt="Properties Popup" width="368" height="128" /></p>
        <p align="center"><img src="/rt/DevTools/DLL_Version_Win7.PNG" alt="Windows 7 Properties Dialog" width="378" height="192" /></p>
      </blockquote>
      <ol>
        <blockquote>
          <p>The highlighted File Version number (3.0.0.0 in these examples) indicates a Perfect Sync version number of 3.00. The final zero indicates a &quot;distribution version&quot;, i.e. not a beta or custom version. </p>
