      <img src="/rt/Dingbats/Dot.Gold.10.gif" width="10" height="10" /> Result Column Info</p>
      <p>The Database Attribute function alone provides well over 250 different values. You also get... </p>
      <p><img src="/rt/Dingbats/Dot.Gold.10.gif" width="10" height="10" /> A powerful set of Error Management functions, including <br />
        <img src="/rt/Dingbats/Dot.Gold.10.gif" width="10" height="10" /> An &quot;ignore specified error&quot; system<br />
        <img src="/rt/Dingbats/Dot.Gold.10.gif" width="10" height="10" />      Six Trace Modes<br />
