<p><img src="/rt/Company/Logos/TC/Area.TC.768.jpg" alt="The Grand Traverse Area" width="768" height="475" /><br />
<span class="Medium">The Grand Traverse area as seen from space, 
including Traverse City, Michigan, USA<br />
</span><span class="Tiny"><em>Courtesy of the Image Science &amp; Analysis Laboratory, 
NASA Johnson Space Center. Modified image  &copy; 2006 Perfect Sync.</em></span></p>
<p>This photo was taken by an astronaut aboard the Space Shuttle <em>Atlantis</em>   in the late autumn of 2002. <br />
  The northern tip of the <a href="http://www.peninsulatownship.com/">Old Mission Peninsula</a> is located  in the very center of the photo. <br />
  The 18-mile peninsula appears to run diagonally in this photo but actually runs north/south, <br />
  parallel to the larger Leelanau Peninsula. The lighthouse at the tip of the <br />
  Old Mission Peninsula is located almost exactly on the 45th parallel, <br />
  halfway between the equator and the north pole.</p>
<p>Grand Traverse Bay is divided by the Old Mission peninsula into East Bay and West Bay. <br />
  Lake Michigan is visible along the top of the photo. Lake Michigan  is one of the <a href="http://en.wikipedia.org/wiki/Great_lakes">Great Lakes</a>.</p>
<p><a href="http://www.ci.traverse-city.mi.us/">Traverse City</a> is located in the lower-left  of the photo, at the base of the Old Mission Peninsula. <br />
  TC is voted among the ten best <em>something</em> in the USA nearly every year. Sleeping Bear Dunes National Lakeshore, <br />
  voted <a href="http://www.sleepingbeardunes.com/">The Most Beautiful Place in America</a>, is partially visible in the very top-left of the photo.
</p>
<p>If you're not from around here, the A in Traverse is pronounced <br />
  like the A in CAT, and the emphasis is on the first syllable.</p>
<p>TRAverse not truhVERSE.</p>
