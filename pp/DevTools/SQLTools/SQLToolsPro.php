    <p class="Medium"><span class="Highlight">SQL Tools Standard</span> includes all of the basic functions that you will need to create PowerBASIC programs that can read and modify SQL databases.</p>
    <p>It allows a PB/Win or PB/CC program to have up to four databases/statements open at the same time.  The entire text-based, single-statement SQL language is supported; SQL Tools Standard does not impose <em>any</em> limitations on the SQL syntax* that a database recognizes. </p>
    <p>Several basic &quot;Info&quot; functions are provided by the Standard version...</p>
